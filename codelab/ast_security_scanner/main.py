"""
main.py

이 파일은 프로그램의 시작점(entry point) 역할을 한다.

사용자는 보통 터미널에서 아래처럼 프로그램을 실행한다.

예:
    python main.py sample.py
    python main.py ./project
    python main.py file1.py file2.py ./src

이 파일의 역할은 다음과 같다.

1. 명령행 인자(sys.argv)로부터 사용자가 입력한 경로를 받는다.
2. 각 경로에서 검사 대상 .py 파일들을 찾는다.
3. 각 파일에 대해 AST 보안 검사를 수행한다.
4. 모든 결과를 모아서 최종 리포트를 출력한다.

즉, main.py는 전체 프로그램 흐름을 제어하는 "오케스트레이션" 역할을 담당한다.
실제 분석은 scanner.py,
파일 탐색은 file_utils.py,
출력은 reporter.py에 맡긴다.
"""

import sys

from scanner import scan_file
from file_utils import find_python_files
from reporter import print_report


def main():
    """
    프로그램의 메인 실행 함수

    처리 순서:
    1. 사용자가 입력한 인자를 확인한다.
    2. 입력된 각 경로에서 파이썬 파일 목록을 수집한다.
    3. 각 파일을 스캔하여 보안 위반 목록을 얻는다.
    4. 전체 결과를 종합하여 리포트를 출력한다.
    """
    # -------------------------------------------------------------------
    # 1) 명령행 인자 검사
    # -------------------------------------------------------------------
    # sys.argv[0]는 현재 실행 중인 파이썬 파일명(main.py)이다.
    # 따라서 실제 사용자 입력 경로는 sys.argv[1:]에 들어 있다.
    #
    # 사용 예:
    #   python main.py sample.py
    #
    # 이 경우:
    #   sys.argv[0] -> "main.py"
    #   sys.argv[1] -> "sample.py"
    # -------------------------------------------------------------------
    if len(sys.argv) < 2:
        print("사용법: python main.py <파일 또는 디렉터리 경로> [추가 경로 ...]")
        return

    # 사용자가 입력한 검사 대상 경로들
    paths = sys.argv[1:]

    # 최종적으로 검사할 모든 파이썬 파일을 저장할 리스트
    all_files = []

    # -------------------------------------------------------------------
    # 2) 각 입력 경로에서 .py 파일 찾기
    # -------------------------------------------------------------------
    for path in paths:
        files = find_python_files(path)

        # 해당 경로에서 찾은 파일들을 전체 목록에 추가
        all_files.extend(files)

    # 중복 경로 제거
    # 예를 들어 사용자가 같은 경로를 중복 입력했을 가능성도 있으므로 set으로 중복 제거
    all_files = sorted(set(all_files))

    # 검사할 파일이 하나도 없으면 안내 후 종료
    if not all_files:
        print("검사할 파이썬 파일이 없습니다.")
        return

    # 전체 파일에서 탐지된 보안 위반 결과를 저장할 리스트
    all_issues = []

    # -------------------------------------------------------------------
    # 3) 각 파일을 하나씩 AST 분석
    # -------------------------------------------------------------------
    for file_path in all_files:
        issues = scan_file(file_path)
        all_issues.extend(issues)

    # -------------------------------------------------------------------
    # 4) 최종 리포트 출력
    # -------------------------------------------------------------------
    print_report(all_issues)


# -------------------------------------------------------------------
# 파이썬 파일이 직접 실행된 경우에만 main()을 호출한다.
#
# 이 조건문이 필요한 이유:
# - python main.py 로 직접 실행할 때는 main()이 실행되어야 한다.
# - 하지만 다른 파일에서 import main 을 했을 때는 자동 실행되면 안 된다.
# -------------------------------------------------------------------
if __name__ == "__main__":
    main()