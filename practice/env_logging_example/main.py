"""
main.py

.env 파일의 환경변수를 python-dotenv로 불러오고
logging 모듈을 이용하여 콘솔 + 파일 로깅을 동시에 수행하는 과제.

요구사항
1. .env 파일에서 LOG_LEVEL, APP_NAME 읽기
2. 로그 파일 : app.log
3. 로그 포맷 : 시간 | 로그레벨 | 메시지
4. 콘솔 + 파일 동시에 출력
5. INFO / DEBUG / ERROR 로그 출력 테스트
"""

# -------------------------------------------------------------
# 1. 필요한 모듈 import
# -------------------------------------------------------------
import os                 # 환경변수 접근을 위한 모듈
import logging            # Python 기본 로깅 모듈
from dotenv import load_dotenv   # .env 파일을 로드하기 위한 모듈


# -------------------------------------------------------------
# 2. .env 파일 로드
# -------------------------------------------------------------
# 현재 실행 경로에 있는 .env 파일을 읽어 환경변수로 등록
load_dotenv()

# 환경 변수 읽기
log_level_str = os.getenv("LOG_LEVEL", "INFO")  # 기본값 INFO
app_name = os.getenv("APP_NAME", "DefaultApp")


# -------------------------------------------------------------
# 3. 문자열 로그 레벨을 logging 레벨로 변환
# -------------------------------------------------------------
# 예: "DEBUG" -> logging.DEBUG
log_level = getattr(logging, log_level_str.upper(), logging.INFO)


# -------------------------------------------------------------
# 4. 로거(Logger) 생성
# -------------------------------------------------------------
logger = logging.getLogger(app_name)

# 로거의 기본 레벨 설정
logger.setLevel(log_level)


# -------------------------------------------------------------
# 5. 로그 포맷 설정
# -------------------------------------------------------------
# 요구사항: 시간 | 로그레벨 | 메시지
log_formatter = logging.Formatter(
    "%(asctime)s | %(levelname)s | %(message)s"
)


# -------------------------------------------------------------
# 6. 파일 핸들러 설정 (app.log)
# -------------------------------------------------------------
file_handler = logging.FileHandler("app.log", encoding="utf-8")

# 파일 핸들러의 로그 레벨 설정
file_handler.setLevel(log_level)

# 포맷 적용
file_handler.setFormatter(log_formatter)


# -------------------------------------------------------------
# 7. 콘솔 핸들러 설정 (터미널 출력)
# -------------------------------------------------------------
console_handler = logging.StreamHandler()

# 콘솔 로그 레벨 설정
console_handler.setLevel(log_level)

# 포맷 적용
console_handler.setFormatter(log_formatter)


# -------------------------------------------------------------
# 8. 로거에 핸들러 등록
# -------------------------------------------------------------
# 이렇게 하면 콘솔 + 파일 동시에 로그가 출력된다
logger.addHandler(file_handler)
logger.addHandler(console_handler)


# -------------------------------------------------------------
# 9. 로그 출력 테스트
# -------------------------------------------------------------
def main():
    """
    프로그램 실행 시 로그 출력 테스트 함수
    """

    # INFO 로그
    logger.info("앱 실행 시작")

    # DEBUG 로그
    logger.debug("환경 변수 로딩 완료")

    # ERROR 로그 테스트 (예외 발생)
    try:
        # 일부러 0으로 나누어 예외 발생
        result = 10 / 0

    except ZeroDivisionError as e:
        logger.error("예외 발생 예시", exc_info=True)


# -------------------------------------------------------------
# 10. 프로그램 실행 진입점
# -------------------------------------------------------------
if __name__ == "__main__":
    main()