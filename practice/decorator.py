import time
from functools import wraps


# -------------------------------------------------------------------
# measure_time 데코레이터
# -------------------------------------------------------------------
# 역할:
#   - 어떤 함수를 감싸서(wrapping)
#   - 그 함수가 실행되는 데 걸린 시간을 측정한 뒤
#   - "함수명 took 0.1234 seconds" 형식으로 출력하고
#   - 원래 함수의 반환값은 그대로 돌려준다.
#
# 매개변수:
#   func -> 데코레이터가 감쌀 "원본 함수"
#
# 반환값:
#   wrapper -> 실제로 원본 함수를 대신 실행할 새로운 함수
# -------------------------------------------------------------------
def measure_time(func):
    # wraps(func):
    #   데코레이터를 적용한 뒤에도 원본 함수의 이름(__name__),
    #   문서 문자열(__doc__) 등의 메타데이터를 보존해 준다.
    #   실무에서는 데코레이터 작성 시 거의 항상 함께 쓰는 편이다.
    @wraps(func)
    def wrapper(*args, **kwargs):
        # -----------------------------------------------------------
        # *args, **kwargs를 사용하는 이유
        # -----------------------------------------------------------
        # 데코레이터는 "어떤 함수든" 감쌀 수 있어야 한다.
        # 그런데 함수마다 매개변수 개수와 형태가 다르다.
        #
        # 예)
        #   f()
        #   g(10)
        #   h(10, 20, name="kim")
        #
        # 이런 다양한 함수를 모두 처리하려면
        # 가변 위치 인자(*args), 가변 키워드 인자(**kwargs)를 받아야 한다.
        # -----------------------------------------------------------

        # 함수 실행 "직전" 시각을 기록
        start_time = time.time()

        # 원본 함수를 실제로 실행
        # 전달받은 인자들을 그대로 넘겨준다.
        result = func(*args, **kwargs)

        # 함수 실행 "직후" 시각을 기록
        end_time = time.time()

        # 총 실행 시간 계산
        elapsed_time = end_time - start_time

        # 과제 요구 형식:
        # "함수명 took 0.1234 seconds"
        # :.4f 는 소수점 넷째 자리까지 표시한다.
        print(f"{func.__name__} took {elapsed_time:.4f} seconds")

        # 매우 중요:
        # 데코레이터는 원래 함수의 "결과"를 바꾸지 않고 그대로 반환해야 한다.
        return result

    return wrapper


# -------------------------------------------------------------------
# 테스트용 함수
# -------------------------------------------------------------------
# 임의의 연산 지연이 있는 함수 slow_function()을 만들고
# 위에서 작성한 measure_time 데코레이터를 적용한다.
#
# @measure_time 은 아래와 같은 의미다.
#   slow_function = measure_time(slow_function)
# -------------------------------------------------------------------
@measure_time
def slow_function():
    # time.sleep(1.5):
    #   실제로 1.5초 동안 프로그램 실행을 잠시 멈춘다.
    #   이를 통해 일부러 "느린 함수"를 만들어 실행 시간 측정이
    #   제대로 되는지 확인할 수 있다.
    time.sleep(1.5)

    # 함수 결과는 그대로 반환되어야 하므로 문자열을 반환
    return "완료!"


# -------------------------------------------------------------------
# 실행부
# -------------------------------------------------------------------
# 데코레이터가 적용된 slow_function()을 호출하면,
# 1) 내부적으로 wrapper 함수가 실행되고
# 2) 원본 함수 slow_function()의 실행 시간을 측정한 뒤
# 3) 실행 시간을 출력하고
# 4) 최종 결과("완료!")를 그대로 반환한다.
# -------------------------------------------------------------------
result = slow_function()

# 반환값이 정상적으로 유지되는지 확인
print("함수 결과:", result)